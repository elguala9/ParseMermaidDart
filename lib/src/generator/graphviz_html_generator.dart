                • <code>&lt;&lt;sealed&gt;&gt;</code> = Sealed class<br>
                • <code>&lt;&lt;mixin&gt;&gt;</code> = Mixin<br>
                • <code>&lt;&lt;enumeration&gt;&gt;</code> = Enum<br>
                • <code>&lt;&lt;extension&gt;&gt;</code> = Extension<br>
                • <code>&lt;&lt;external&gt;&gt;</code> = External library class
            </p>
        </div>
    </div>

    <footer>
        <p>Generated by diagram_dart • Graphviz DOT rendering via kroki.io</p>
    </footer>

    <script>
        function handleError() {
            document.getElementById('diagram-container').innerHTML =
                '<div class="error"><p><strong>Error rendering diagram</strong></p><p>Unable to render Graphviz diagram. Please check your internet connection or try again later.</p></div>';
        }
    </script>
</body>
</html>''';
  }
}
